#
# Tipos de datos : Booleanos.
#                  Operadores lógicos y relacionales.
#
switch1 = True
print(switch1)
#
switch2 = False
print(switch2)
# Los operadores lógicos :
print(switch1 and switch2)
# O de una forma más legible:
print(switch1,'AND', switch2,' ES ',switch1 and switch2)
print(switch1,'OR', switch2,' ES ',switch1 or switch2)
