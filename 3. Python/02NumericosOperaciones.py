# División 
s1 = int(input("Dame un número entero "));
s2 = int(input("Dame otro número entero "));
division =  s1 / s2;
print("El resultado es :", division);
# Redondeando
redondeo = int(input("A cuántas cifras quieres redondear el resultado anterior? "));
resultado = round(division,redondeo)
print(" El resultado es :", resultado)
