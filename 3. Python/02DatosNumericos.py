# Tipos de datos: Numéricos 
# 
# Suma de números enteros
s1 = int(input("Dame un número entero "));
s2 = int(input("Dame otro número entero "));
suma =  s1 + s2;
print("El resultado es :", suma, "o tb. :", s1 + s2);
# Lo mismo pero con números reales
s1 = float(input("Dame un número real  "));
s2 = float(input("Dame otro número real  "));
suma =  s1 + s2;
print("El resultado es :", suma, "o tb. :", s1 + s2);
# Multiplicación de números enteros
s1 = int(input("Dame un número entero "));
s2 = int(input("Dame otro número entero "));
Multiplicando =  s1 * s2;
print("El resultado es :", Multiplicando, "o tb. :", s1 * s2);
